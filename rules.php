      <h2>Illegal and Legal Goods</h2>
      <p>
        Illegal goods refer to products or items that are prohibited by law to buy, sell, possess, or distribute. These goods are typically associated with criminal activities and can cause harm to individuals, society, or the environment. Examples of illegal goods include:
      </p>
      <ul>
        <li>Illegal drugs and narcotics</li>
        <li>Counterfeit goods</li>
        <li>Stolen items</li>
        <li>Firearms and weapons</li>
        <li>Endangered animal products</li>
      </ul>
      
      <p>
        Legal goods, on the other hand, are products or items that comply with the laws and regulations of a particular jurisdiction. These goods can be traded, bought, sold, and possessed without legal consequences. Examples of legal goods include:
      </p>
      <ul>
        <li>Food and beverages</li>
        <li>Clothing and accessories</li>
        <li>Electronics</li>
        <li>Vehicles</li>
        <li>Books and media</li>
      </ul>
    </section>

